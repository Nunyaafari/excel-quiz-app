import { ImageResponse } from 'next/og'
import { decodeShareResultsToken } from '@/lib/share-results'

export const alt = 'Excel Mastery Quiz share badge'
export const runtime = 'nodejs'
export const size = {
  width: 1200,
  height: 630,
}
export const contentType = 'image/png'

type OpenGraphImageProps = {
  params: Promise<{
    token: string
  }>
}

export default async function OpenGraphImage({ params }: OpenGraphImageProps) {
  const { token } = await params
  const snapshot = decodeShareResultsToken(token)

  const percentage = snapshot?.percentage ?? 0
  const performanceLabel = snapshot?.performanceLabel ?? 'Performance Summary'
  const correctAnswers = snapshot?.correctAnswers ?? 0
  const totalQuestions = snapshot?.totalQuestions ?? 0
  const profileLabel = snapshot?.profileLabel ?? 'Mixed Difficulty'
  const completedLabel = snapshot?.completedLabel ?? 'Recently'
  const scoreColor = percentage >= 80 ? '#16a34a' : percentage >= 60 ? '#d97706' : '#dc2626'

  return new ImageResponse(
    (
      <div
        style={{
          width: '100%',
          height: '100%',
          display: 'flex',
          background: '#0f2744',
          color: 'white',
          fontFamily: 'Arial, sans-serif',
          padding: 44,
          boxSizing: 'border-box',
        }}
      >
        <div
          style={{
            width: '100%',
            height: '100%',
            borderRadius: 32,
            background: '#144d6a',
            display: 'flex',
            padding: 36,
            justifyContent: 'space-between',
            boxSizing: 'border-box',
          }}
        >
          <div style={{ display: 'flex', flexDirection: 'column', width: 390 }}>
            <div style={{ fontSize: 24, fontWeight: 700, letterSpacing: 4, color: '#C8E8FF' }}>EXCEL SKILLS BADGE</div>
            <div
              style={{
                marginTop: 32,
                display: 'flex',
                flexDirection: 'column',
                background: 'white',
                borderRadius: 28,
                padding: '32px 28px',
                boxSizing: 'border-box',
              }}
            >
              <div style={{ fontSize: 96, fontWeight: 700, color: scoreColor }}>{percentage}%</div>
              <div style={{ marginTop: 8, fontSize: 34, fontWeight: 700, color: '#183251' }}>{performanceLabel}</div>
              <div style={{ marginTop: 18, fontSize: 26, color: '#5A6F8A' }}>
                {correctAnswers}/{totalQuestions} correct
              </div>
            </div>
          </div>

          <div
            style={{
              display: 'flex',
              flexDirection: 'column',
              justifyContent: 'center',
              width: 620,
              paddingLeft: 18,
              boxSizing: 'border-box',
            }}
          >
            <div style={{ fontSize: 60, fontWeight: 700, lineHeight: 1.1 }}>Dare to Take the Quiz</div>
            <div style={{ marginTop: 22, fontSize: 30, color: '#D6EBFF' }}>{profileLabel}</div>
            <div style={{ marginTop: 14, fontSize: 26, color: '#D6EBFF' }}>Completed {completedLabel}</div>
            <div style={{ marginTop: 30, fontSize: 24, color: '#D6EBFF' }}>Generated by Excel Mastery Quiz</div>
          </div>
        </div>
      </div>
    ),
    {
      ...size,
    }
  )
}
